<script>
import CartItem from "../components/CartItem.vue"
import Header from "../components/Header.vue";
import Footer from "../components/Footer.vue";
import BackToTop from "../components/BackToTop.vue";

import CartAPI from "../api/cart"
import BookAPI from "../api/book"
import AddressAPI from "../api/address"
import UserAPI from "../api/user"

export default {
    components: {
        CartItem,
        Header,
        Footer,
        BackToTop
    },
    data: () => {
        return {
            cartList: [],
            subTotal: null,
            shiping: null,
            total: null,
            address: null,
            name: null
        }
    },
    methods: {
        async changeQuantity(e, item) {
            if(e) {
                this.subTotal += item
                this.total += item
            } else {
                this.subTotal -= item
                this.total -= item
            }
        }
    },
    async created() {
        const item = await CartAPI.fetchAllItem()
        item.forEach(async(element) => {
            const data = await BookAPI.getBookByID(element.book)
            const list = {
                element,
                data
            }
            this.cartList.push(list)
            const temp = data.price * element.quantity
            this.subTotal += temp
            this.shiping += 20000
            this.total += (temp + 20000)
        })

        this.address = (await AddressAPI.getAddress()).data.pop()

        this.name = (await UserAPI.rememberUser().full_name
        console.log(this.name)
    },
}
</script>