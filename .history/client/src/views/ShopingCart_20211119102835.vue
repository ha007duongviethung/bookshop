        <div class="content cart">
            <div class="container px-4 py-5 mx-auto">
                <div class="row cart-header d-flex justify-content-center align-items-center pb-2">
                    <div class="col-6">
                        <h4 class="heading">Giỏ hàng</h4>
                    </div>
                    <div class="col-6">
